public class Book
{
  private String author;
  private String title;
  private double price;
  private int pages;

public Book( )
{
  this.author = author;
  this.title = title;
  this.price = price;
  this.pages = pages;


  }
  public void setPrice(double price){
  this.price = price;
}
  public String getAuthor(){
    return author;
}
public String getTitle(){
    return title;
}
}
