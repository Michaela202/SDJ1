package Person;

public class Person
{
  private String name;
  private String birthday;

  public void setBirthday(String birthday)
  {
    this.birthday = birthday;
  }

  public void setName(String name)
  {
    this.name = name;
  }

  public String getBirthday()
  {
    return birthday;
  }

  public String getName()
  {
    return name;
  }
}

